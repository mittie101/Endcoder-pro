// utils/escape.js - Shared HTML-escaping utility (loaded before all renderer scripts)
/* exported escapeHtml */
function escapeHtml(text) {
  return String(text)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
